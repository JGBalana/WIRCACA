    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        //Insert data
        String sql = "INSERT INTO users(name, username, password, is_admin, student_id) VALUES(?,?,?,?, ?)";
        try {
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, txtName.getText());
            pst.setString(2, txtUsername.getText());
            pst.setString(3, txtPassword.getText());
            pst.setInt(4, cbAdmin.getSelectedItem().toString().equals("True") ? 1 : 0);
            final int studentId = Integer.parseInt(txtStudentId.getText());
            if (studentId > 0) {
                pst.setInt(5, studentId);
            } else {
                pst.setNull(5, Types.INTEGER);
            }
            pst.execute();
            JOptionPane.showMessageDialog(null, "User Successfully Added!");
            this.dispose();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

